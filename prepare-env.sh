# 1. install Node.js
#       |
#       |- download & install from: https://nodejs.org/en/
#
# 2. check node / npm:
#       |
#       |- node -v
#       |- npm -v
#
# 3. install webpack:
#       |
#       |- npm install -g webpack
#
# 4. install TypeScript:
#       |
#       |- npm install -g typescript
#
#
# Now, you can do:
#


# install npm package for this project.
#
npm install


# link global typescript to this project.
#
npm link typescript


#
# finally, you can run:
#
#   webpack
#
# or
#
#   npm run build
#
# to build project.
#
#
# run:
#
#   npm run start
#
# to run project.
#
